    len = array->Length;
    while( len )
    {
        ms_array_nrpop( array );
        cr_assert_eq( array->Length, --len );
    }
    // spróbuj usunąć nieistniejący element
    ms_array_nrpop( array );
    cr_assert_eq( array->Length, 0 );


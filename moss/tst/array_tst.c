        cr_assert_fail( "ms_array_init failed with code: %d", ercode );

    // powiększanie dokładne - tyle ile potrzeba w aktualnym momencie
    array.Increator = NULL;

    // dodaj pierwszy element
    number = 15;
    if( (ercode = ms_array_mpush(&array, &number)) )
        cr_assert_fail( "ms_array_mpush failed with code: %d", ercode );
    
    cr_assert_eq( array.Length, 1 );
    cr_assert_eq( array.Capacity, 2 );

